        // primitive types
        // byte = 1 byte memory (-128 to 127)
        // short = 2 byte memory
        // int = 4 byte memory (1,2,3,4...)
        // long = 8 byte memory
        // float = 4 byte memory (3.14)
        // double = 8 byte memory
        // char = 2 byte memory (a,b,c,d...)
        // boolean = 1 byte memory (True/False)
        byte age = 69;
        int phone = 168426942;
        long phone2 = 168426942323425L;
        float pi = 3.141592653589793F;
        char letter = 'a';
        boolean isAdult = true;

        // non-primitive type
        String name = "Anonto";
        String city = new String("Dhaka");
